    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        Producto product = new Producto();
        int id = Integer.parseInt(txtid.getText());
        product.setId(id);
        product.setName(txtname.getText());
        product.setCategory((String) jComboBox1.getSelectedItem());
        product.setPrice(txtprice.getText());
        BD_Productos.update(product);
        setVisible(false);
        new GestionarProductos().setVisible(true);
    }//GEN-LAST:event_btnupdateActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        String id = txtid.getText();
        int a = JOptionPane.showConfirmDialog(null, " ¿Está seguro de querer eliminar este producto?", "Select", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            BD_Productos.delete(id);
            setVisible(false);
            new GestionarProductos().setVisible(true);
        }
    }//GEN-LAST:event_btndeleteActionPerformed

