    private void btnloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloginActionPerformed
        // TODO add your handling code here:
        String email = txtemail.getText();
        String password = txtpassword.getText();
        Usuario user = null;
        user = BD_Usuario.login(email, password);
        if(user == null)
            JOptionPane.showMessageDialog(null, "<html><b style=\"color:red\">Usuario o contraseña incorrectos</b></html>","Message",JOptionPane.ERROR_MESSAGE);
        else{
            if(user.getStatus().equals("false")){
                ImageIcon icon = new ImageIcon("src/popupicons/wait.png");
                JOptionPane.showMessageDialog(null,"<html><b>Espere aprobación del administrador.</b></html>","Message",JOptionPane.INFORMATION_MESSAGE,icon);
                clear();
                
            }
            if(user.getStatus().equals("true")){
                setVisible(false);
                new VentanaPrincipal(email).setVisible(true);
                
            }
        }
    }//GEN-LAST:event_btnloginActionPerformed

