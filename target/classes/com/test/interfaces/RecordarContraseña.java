    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        String answer = txtans.getText();
        String newPassword = txtpassword.getText();
        if(answer.equals(dbAnswer)){
            BD_Usuario.update(email, newPassword);
            clear();
        }else{
            JOptionPane.showMessageDialog(null, "<html><b style=\"colorred\">Contrase単a incorrecta.</b></html>","Message",JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnupdateActionPerformed

    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // TODO add your handling code here:
        email = txtemail.getText();
        Usuario user = null;
        user = BD_Usuario.getSecurityQuestion(email);
        if(user == null)
        {
            JOptionPane.showMessageDialog(null, "<html><b style=\"colorred\">Correo electr坦nico incorrecto.</b></html>","Message",JOptionPane.ERROR_MESSAGE);
            
        }
        else{
            btnsearch.setEnabled(false);
            txtemail.setEditable(false);
            dbAnswer = user.getAnswer();
            txtsq.setText(user.getSecurityQuestion());
            validateFields();
            
        }
    }//GEN-LAST:event_btnsearchActionPerformed

